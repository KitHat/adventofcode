fn solve(boards: Vec<Vec<Vec<u32>>>, draws: Vec<u32>) -> u32 {
    let prep = prep(&boards);
    let mut weigths_prep = draws.clone().into_iter().enumerate().collect::<Vec<(usize, u32)>>();
    weigths_prep.sort_by(|a, b| a.1.cmp(&b.1));
    let weigths: Vec<usize> = weigths_prep.into_iter().map(|(pos, _)| pos).collect();
    let prep2 = prep.iter().map(|(i, v)| (i, eval(v, &weigths)))
        .fold(vec![std::u32::MAX; boards.len()], |mut acc, (i, w)| {
            if acc[i.clone() as usize] > w as u32 {acc[i.clone() as usize] = w as u32}
            acc
        });
    let (i, weight_fin) = prep2.iter().enumerate().max_by(|a, b| a.1.cmp(&b.1)).unwrap();
    let fin = draws[weight_fin.clone() as usize];
    let mut sum: u32 = 0;
    println!("{:?}", boards[i.clone() as usize]);
    println!("wf {}", weight_fin);
    for j in 0..5 {
        for k in 0..5 {
            let val = boards[i.clone() as usize][j as usize][k as usize]; 
            if weigths[val as usize] > weight_fin.clone() as usize {
                println!("{} {}", draws[val as usize], val);
                sum += val;
            }
        }
    }
    println!("{} {}",sum, fin);
    fin*sum
}

fn eval(row: &Vec<u32>, draws:&Vec<usize>) -> usize {
    row.iter().map(|a| draws[a.clone() as usize]).max().unwrap()
}

fn prep(boards: &Vec<Vec<Vec<u32>>>) -> Vec<(u32, Vec<u32>)> {
    let mut  verts: Vec<(u32, Vec<u32>)> = boards.iter().enumerate().map(|(i, brd)| {
        let mut res: Vec<(u32, Vec<u32>)> = vec![];
        for k in 0..5 {
            let mut inner: Vec<u32> = vec![];
            for j in 0..5 {
                inner.push(brd[j][k]);
            }
            res.push((i as u32, inner));
        }
        res
    }).flat_map(|a| a).collect();
    let mut hors: Vec<(u32, Vec<u32>)> = boards.iter().enumerate().map(|(a,v)| {v.iter().map(move |v1| (a as u32, v1.clone()))}).flatten().collect();
    verts.append(&mut hors);
    verts
}

