fn eval(input: Vec<Line>) -> usize {
    let (diag, other): (Vec<Line>, Vec<Line>) = input.iter().partition(|a| (a.sa - a.fa).abs() == (a.sb - a.fb).abs());
    let coord_map = other
        .iter()
        .filter(|a| a.sa == a.fa || a.sb == a.fb)
        .fold(HashMap::<(i32, i32), u32>::new(), |mut acc, next| {
            let ra = if next.sa > next.fa { next.fa..next.sa+1 } else { next.sa..next.fa+1};
            let rb = if next.sb > next.fb { next.fb..next.sb+1 } else { next.sb..next.fb+1};
            for i in ra {
                for j in rb.clone() {
                    if let Some(val) = acc.get_mut(&(i, j)) {
                        *val += 1;
                    } else {
                        acc.insert((i, j), 1);
                    }
                }
            }
            acc
        });
    let coord_map_fin = diag.iter()
        .fold(coord_map, |mut acc, next| {
            let ra = if next.sa > next.fa {(next.fa..next.sa+1).rev().collect::<Vec<i32>>()} else {(next.sa..next.fa+1).collect::<Vec<i32>>()};
            let rb = if next.sb > next.fb {(next.fb..next.sb+1).rev().collect::<Vec<i32>>()} else {(next.sb..next.fb+1).collect::<Vec<i32>>()};
            for (i,j) in ra.into_iter().zip(rb.into_iter()) {
                    if let Some(val) = acc.get_mut(&(i, j)) {
                        *val += 1;
                    } else {
                        acc.insert((i, j), 1);
                    }
            }
            acc
        });
    // println!("{:?}", coord_map_fin);
    coord_map_fin.iter().filter(|(k,v)| **v > 1).count()
}

