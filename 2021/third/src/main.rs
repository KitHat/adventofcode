fn solve(input: Vec<String>) -> String {
    input.iter().map(|s| s.chars()).fold(vec![vec![0,0]], |mut res, mut next| {
        let mut i = 0;
        while let Some(ch) = next.next() {
            if let None = res.get(i) {
                res.insert(i, vec![0, 0]);
            }
            let num: usize = ch.to_string().parse::<usize>().unwrap();
            res[i][num] += 1;
            i += 1;
        }
        res
    }).iter().map(|char_vec| if char_vec[0] > char_vec[1] { "0" } else { "1" }).collect::<String>()
}

fn solve_2(input: Vec<String>) -> (String, String) {
    let len = input[0].len();
    let prep = input.iter().map(|s| (s.clone(), s.chars().map(|ch| ch.to_string().parse::<usize>().unwrap()).collect::<Vec<usize>>())).collect::<Vec<(String, Vec<usize>)>>();
    (find(prep.clone(), len, true), find(prep.clone(), len, false))
}

fn find(mut input: Vec<(String, Vec<usize>)>, len: usize, gamma: bool) -> String {
    for i in 0..len {
        let cont = input.iter().map(|(_, vc)| vc[i]).fold(vec![0,0], |mut acc, next| {acc[next] += 1; acc});
        let comm = if gamma {
            if cont[0] <= cont[1] {
                1
            } else {
                0
            }
        } else {
            if cont[0] > cont[1] {
                1
            } else {
                0
            }
        };
        input = input.into_iter().filter(|(_, vc)| vc[i] == comm).collect();
        if input.len() == 1 {
            return input[0].0.clone()
        }
    }
    input[0].0.clone()
}

